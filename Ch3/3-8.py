#List five places to visit throughout the world. 
visits = ['dublin' , 'scotland' , 'australia' , 'russia' , 'fiji']

#Print list in original order. 
print(visits)

#Print list sorted in alphabetical order. 
print(sorted(visits))

#Print list in original order again. 
print(visits)

#Print in reverse order. 
visits.reverse()
print(visits)


#Reverse order again. 
visits.reverse()
print(visits)


#Sort list permanently in alphabetical order.
visits.sort() 
print(visits)


#Re-sort list permanently in reverse alphabetical order.
visits.sort(reverse=True) 
print(visits)